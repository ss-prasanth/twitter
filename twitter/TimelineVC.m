#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
}

-(CGFloat)tableView:(UITableView *)tableView heightForRowAtIndexPath:(NSIndexPath *)indexPath{
    return 92;
}


#pragma mark - Private methods

- (void)onSignOutButton {
    [User setCurrentUser:nil];
}

- (void)reload {
    [[TwitterClient instance] homeTimelineWithCount:20 sinceId:0 maxId:0 success:^(AFHTTPRequestOperation *operation, id response) {
      //  NSLog(@"%@", response);
        self.tweets = [Tweet tweetsWithArray:response];
        [self.tableView reloadData];
    } failure:^(AFHTTPRequestOperation *operation, NSError *error) {
        // Do nothing
    }];
}

- (void)onComposeButton {
    ComposeViewController * composeViewController = [[ComposeViewController alloc] init];
    UINavigationController *composeNavController = [[UINavigationController alloc]initWithRootViewController:composeViewController];
    //ComposeViewController.delegate = self;
    [self presentViewController:composeNavController animated:YES completion:nil];
}

#pragma mark ComposeViewControllerDelegate methods
-(void)dismissComposeView{
    [self dismissViewControllerAnimated:NO completion:nil];
    [self reload];
}

@end
